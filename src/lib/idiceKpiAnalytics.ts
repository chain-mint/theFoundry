export type KpiStatus = "on_track" | "watch" | "off_track";
export type KpiTargetType = "min" | "max" | "range";
export type KpiUnit = "count" | "percent" | "currency" | "ratio";

export interface KpiDefinition {
  kpiId:
    | "job_creation"
    | "institutional_growth"
    | "gender_parity"
    | "geographic_spread"
    | "economic_contribution"
    | "startup_survival_12_24m";
  label: string;
  description: string;
  unit: KpiUnit;
  targetType: KpiTargetType;
  target: number;
  owner: "BOI" | "Admin" | "Program Management";
  updateCadence: "monthly" | "quarterly";
  source: string;
  formula: string;
}

export interface KpiSnapshot {
  kpiId: KpiDefinition["kpiId"];
  label: string;
  unit: KpiUnit;
  target: number;
  actual: number;
  variance: number;
  status: KpiStatus;
}

export interface KpiTrendPoint {
  month: string;
  jobsCreated: number;
  genderParity: number;
  geographicSpread: number;
}

export interface KpiFramework {
  definitions: KpiDefinition[];
  snapshots: KpiSnapshot[];
  trend: KpiTrendPoint[];
  breakdownByState: KpiBreakdownRecord[];
  breakdownByCohort: KpiBreakdownRecord[];
  breakdownBySector: KpiBreakdownRecord[];
  targetHistory: KpiTargetRevision[];
  generatedAt: string;
}

export interface KpiBreakdownRecord {
  label: string;
  startups: number;
  jobsCreated: number;
  complianceRate: number;
  femaleLedSharePct: number;
}

export interface KpiTargetRevision {
  kpiId: KpiDefinition["kpiId"];
  previousTarget: number;
  nextTarget: number;
  changedAt: string;
  reason: string;
  changedBy: string;
}

