function classifyStatus(
  actual: number,
  target: number,
  targetType: KpiTargetType,
): KpiStatus {
  if (targetType === "min") {
    if (actual >= target) return "on_track";
    if (actual >= target * 0.9) return "watch";
    return "off_track";
  }

  if (targetType === "max") {
    if (actual <= target) return "on_track";
    if (actual <= target * 1.1) return "watch";
    return "off_track";
  }

  return "watch";
}

export function buildIdiceKpiFramework(params: {
  portfolio: BoiPortfolioRecord[];
  geoSpread: BoiStatePlacement[];
  geoTrend: GeoTrendSnapshotRecord[];
  startupSectors?: Record<string, string>;
}): KpiFramework {
  const geo = buildGeoSpreadInsights(params.geoSpread, {
    alertLimit: 10,
    topLimit: 12,
  });
  const jobsCreated = params.portfolio.reduce(
    (sum, item) => sum + item.jobsCreated,
    0,
  );
  const complianceVerified = params.portfolio.filter(
    (item) => item.compliance === "Verified",
  ).length;
  const unlockedGrants = params.portfolio.filter(
    (item) => item.grantStatus === "Unlocked",
  ).length;
  const startupCount = params.portfolio.length;

  const complianceScore =
    startupCount === 0 ? 0 : (complianceVerified / startupCount) * 100;
  const unlockScore =
    startupCount === 0 ? 0 : (unlockedGrants / startupCount) * 100;
  const activityScore = Math.min(100, startupCount * 8);
  const institutionalGrowth = Math.round(
    activityScore * 0.35 + complianceScore * 0.4 + unlockScore * 0.25,
  );

  const genderParity = geo.summary.femaleLedSharePct;
  const geographicSpread = geo.summary.coveragePct;

  const economicContribution =
    jobsCreated * 1300 +
    params.portfolio.reduce((sum, item) => sum + item.talentPlaced, 0) * 250 +
    params.geoSpread.reduce((sum, item) => sum + item.activeStartups, 0) *
      18000;

  const activeStartups = params.geoSpread.reduce(
    (sum, item) => sum + item.activeStartups,
    0,
  );
  const baselineStartups = params.geoSpread.reduce(
    (sum, item) => sum + Math.max(item.totalStartups, 0),
    0,
  );
  const startupSurvival =
    baselineStartups === 0
      ? 0
      : Math.round((activeStartups / baselineStartups) * 100);

  const values: Record<KpiDefinition["kpiId"], number> = {
    job_creation: jobsCreated,
    institutional_growth: institutionalGrowth,
    gender_parity: genderParity,
    geographic_spread: geographicSpread,
    economic_contribution: Math.round(economicContribution),
    startup_survival_12_24m: startupSurvival,
  };

  const snapshots: KpiSnapshot[] = KPI_DEFINITIONS.map((def) => {
    const actual = values[def.kpiId];
    return {
      kpiId: def.kpiId,
      label: def.label,
      unit: def.unit,
      target: def.target,
      actual,
      variance: Number((actual - def.target).toFixed(2)),
      status: classifyStatus(actual, def.target, def.targetType),
    };
  });

  const trend: KpiTrendPoint[] = params.geoTrend.map((point) => ({
    month: point.month,
    jobsCreated: point.jobsCreated,
    genderParity: point.femaleLedSharePct,
    geographicSpread: point.coveragePct,
  }));

  const stateMap = new Map<string, KpiBreakdownRecord>();
  params.geoSpread.forEach((item) => {
    const stateJobs = params.portfolio
      .filter((entry) => entry.state === item.state)
      .reduce((sum, entry) => sum + entry.jobsCreated, 0);
    const femaleLedSharePct =
      item.totalStartups === 0
        ? 0
        : Math.round((item.femaleLedStartups / item.totalStartups) * 100);
    stateMap.set(item.state, {
      label: item.state,
      startups: item.activeStartups,
      jobsCreated: stateJobs,
      complianceRate: item.complianceRate,
      femaleLedSharePct,
    });
  });

  const cohortMap = new Map<string, KpiBreakdownRecord>();
  params.portfolio.forEach((item) => {
    const existing = cohortMap.get(item.cohort) ?? {
      label: item.cohort,
      startups: 0,
      jobsCreated: 0,
      complianceRate: 0,
      femaleLedSharePct: 0,
    };
    existing.startups += 1;
    existing.jobsCreated += item.jobsCreated;
    existing.complianceRate += item.compliance === "Verified" ? 100 : 0;
    cohortMap.set(item.cohort, existing);
  });
  const breakdownByCohort = Array.from(cohortMap.values()).map((item) => ({
    ...item,
    complianceRate:
      item.startups === 0 ? 0 : Math.round(item.complianceRate / item.startups),
  }));

  const sectorMap = new Map<string, KpiBreakdownRecord>();
  params.portfolio.forEach((item) => {
    const sector = params.startupSectors?.[item.startup] ?? "Unknown";
    const existing = sectorMap.get(sector) ?? {
      label: sector,
      startups: 0,
      jobsCreated: 0,
      complianceRate: 0,
      femaleLedSharePct: 0,
    };
    existing.startups += 1;
    existing.jobsCreated += item.jobsCreated;
    existing.complianceRate += item.compliance === "Verified" ? 100 : 0;
    sectorMap.set(sector, existing);
  });
  const breakdownBySector = Array.from(sectorMap.values()).map((item) => ({
    ...item,
    complianceRate:
      item.startups === 0 ? 0 : Math.round(item.complianceRate / item.startups),
  }));

  const breakdownByState = Array.from(stateMap.values()).sort(
    (a, b) => b.jobsCreated - a.jobsCreated,
  );

  return {
    definitions: KPI_DEFINITIONS,
    snapshots,
    trend,
    breakdownByState,
    breakdownByCohort,
    breakdownBySector,
    targetHistory: KPI_TARGET_HISTORY,
    generatedAt: new Date().toISOString(),
  };
}
